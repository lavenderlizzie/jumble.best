export function getAllWords() {
  return allWords;
}

export function getWordsFromCategory(category) {
  if (category === 'oxford') return allOxfordWords;
  if (category === 'slang') return allSlangWords;
  return allWords;
}

export function getDefinition(word, category) {
  const w = word.toLowerCase();
  if (category === 'oxford') return oxfordDictionary[w] || "Definition not available";
  if (category === 'slang') return slangDictionary[w] || "Definition not available";
  return oxfordDictionary[w] || slangDictionary[w] || (allStandardWords.includes(w) ? word : "Definition not available");
}

export function findWordsMatchingPattern(pattern, category = 'standard') {
  if (!pattern || pattern.trim() === '') return [];
  const cleanPattern = pattern.toLowerCase();
  if (/[^a-z_]/.test(cleanPattern)) return [];
  const words = getWordsFromCategory(category);
  return words.filter(word => {
    if (word.length !== cleanPattern.length) return false;
    for (let i = 0; i < cleanPattern.length; i++) {
      if (cleanPattern[i] !== '_' && cleanPattern[i] !== word[i]) return false;
    }
    return true;
  });
}

export function unscrambleLetters(letters, category = 'standard') {
  if (!letters || letters.trim().length < 2) return [];
  const cleanLetters = letters.toLowerCase().replace(/[^a-z]/g, '');
  if (cleanLetters.length < 2) return [];
  const inputCounts = {};
  for (const char of cleanLetters) inputCounts[char] = (inputCounts[char] || 0) + 1;
  const words = getWordsFromCategory(category);
  return words.filter(word => {
    if (word.length !== cleanLetters.length) return false;
    const wordCounts = {};
    for (const char of word) {
      wordCounts[char] = (wordCounts[char] || 0) + 1;
      if (!inputCounts[char] || wordCounts[char] > inputCounts[char]) return false;
    }
    return true;
  });
}

export function findAnagrams(letters, category = 'standard') {
  if (!letters || letters.trim().length < 2) return [];
  const cleanLetters = letters.toLowerCase().replace(/[^a-z]/g, '');
  if (cleanLetters.length < 2) return [];
  const letterCounts = {};
  for (const char of cleanLetters) letterCounts[char] = (letterCounts[char] || 0) + 1;
  const words = getWordsFromCategory(category);
  return words.filter(word => {
    if (word.length > cleanLetters.length) return false;
    const wordCounts = {};
    for (const char of word) {
      wordCounts[char] = (wordCounts[char] || 0) + 1;
      if (!letterCounts[char] || wordCounts[char] > letterCounts[char]) return false;
    }
    return true;
  });
}
